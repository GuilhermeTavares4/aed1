quantPequena = int(input("Digite a quantidade de camisetas pequenas: "))
quantMedia = int(input("Digite a quantidade de camisetas médias: "))
quantGrande = int(input("Digite a quantidade de camisetas grandes: "))
valorArrecadado = quantPequena * 10 + quantMedia * 12 + quantGrande * 15
print (f"O valor que será arrecadado é R${valorArrecadado}")